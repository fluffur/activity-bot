package help

import (
	"activity-bot/internal/helpers"
	"context"

	"github.com/go-telegram/bot"
	"github.com/go-telegram/bot/models"
)

type Handler struct {
}

func NewHandler() *Handler {
	return &Handler{}
}

func (h *Handler) Start(ctx context.Context, b *bot.Bot, update *models.Update) {
	helpers.AnswerMessage(ctx, b, update, "Привет! Я могу следить за еженедельной нормой сообщений в группе. Добавь меня в группу или введи команду /help.")
}

func (h *Handler) Help(ctx context.Context, b *bot.Bot, update *models.Update) {
	helpText := `
📌 <b>Команды бота для работы с нормой сообщений, рестами и ролями</b>

━━━━━━━━━━━━━━
💬 <b>Норма чата</b>
<pre>
норма           — показать текущую норму сообщений
норма (число)   — установить новую норму (например: !норма 50)
</pre>

━━━━━━━━━━━━━━
🛌 <b>Рест (освобождение от нормы)</b>
<pre>
рест            — показать ваш текущий статус реста
рест (период)   — поставить рест (например: !рест неделя, !рест 2 недели)
-рест           — завершить рест досрочно
</pre>

━━━━━━━━━━━━━━
🛡 <b>Роли и администрация</b>
<pre>
роль                  — показать вашу роль
роль @user            — показать роль пользователя
роль @user (название) — установить роль (только админы)
роли                  — список всех участников с ролями
админы                — список администраторов бота
админ @user           — добавить администратора
-админ @user          — удалить администратора
обновить чат          — синхронизировать участников и титулы
</pre>

━━━━━━━━━━━━━━
📊 <b>Отчёты</b>
<pre>
отчёт — получить еженедельный отчёт по активности
</pre>

━━━━━━━━━━━━━━
❓ <b>Вопросы</b>
<i>Как считаются сообщения?</i>
Сообщения считаются один к одному. Подсчёт только среди пользователей, не находящихся в ресте.

💡 Команды поддерживают префиксы !, /, ., + (например: !рест, /норма, .роль)

`

	helpers.AnswerMessage(ctx, b, update, helpText)
}
